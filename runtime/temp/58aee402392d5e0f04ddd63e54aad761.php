                            <div class="article bottom-line">
                                <div class="title">
                                    <strong><span class="wysiwyg-font-size-large">隐私权政策适用范围</span></strong></div>
                                <div>
                                    <p class="paragraph-title mt20"><strong>包括18ico如何收集、处理、保护：</strong></p>
                                    <ul>
                                        <li>
                                            <p class="li-paragraph">用户在登录本网站和服务器时留下的个人身份信息；</p>
                                        </li>
                                        <li>
                                            <p class="li-paragraph">用户通过本网站和服务器与其他用户或非用户之间传送的各种资讯；</p>
                                        </li>
                                        <li>
                                            <p class="li-paragraph">18ico与商业伙伴共享的其他用户或非用户的各种信息。</p>
                                        </li>
                                    </ul>
                                </div>
                            </div>
                            <div class="article bottom-line">
                                <div class="title">
                                    <strong><span class="wysiwyg-font-size-large">信息收集、使用和认证</span></strong></div>
                                <div>
                                    <p class="paragraph-title"><strong>您提供的信息</strong></p>
                                    <p class="paragraph mt20">
                                        在您登记注册18ico帐户，或使用该帐户，或参加其他18ico及与之相关需要注册的服务或推广活动时，我们会要求您提供个人信息(包括但不限于您的电子邮件地址、帐户密码以及昵称等)。这些信息会以加密方式保存在安全的服务器上。我们会将从您的帐户下采集的个人信息与其他从18ico服务中或从第三方获得的信息和资讯进行整合，以便向您提供更好的用户体验和改善我们的服务质量。在某些服务中，我们会给予提示，由您亲自决定是否参与上述信息的整合。</p>
                                    <p class="paragraph-title mt20"><strong>身份认证</strong></p>
                                    <p class="paragraph mt20">
                                        您同意18ico可以自行或通过合作的第三方机构对您提交或18ico搜集的用户信息（包括但不限于您的个人身份证信息等）进行核实，并对获得的核实结果根据本协议及有关文件进行查看、使用和留存等操作。</p>
                                    <p class="paragraph-title mt20"><strong>日志信息</strong></p>
                                    <p class="paragraph mt20">
                                        当您使用18ico的服务时，我们的主机会自动记录您的浏览器在访问网站时所发送的信息。主机日志信息包括但不限于您的网路请求、IP地址、浏览器类型、浏览器使用的语言、请求的日期和时间，以及一个或多个可以对您的浏览器进行辨识的cookie。</p>
                                    <p class="paragraph-title mt20"><strong>用户交流</strong></p>
                                    <p class="paragraph mt20">
                                        当您与18ico通过电子邮件或其他方式进行交流时，我们可能会记录这些交流内容用以处理您的问题以及改善我们的服务。</p>
                                    <p class="paragraph mt20">
                                        18ico仅对本隐私权政策和/或具体服务的隐私声明中允许的目的而对用户的个人信息进行处理。除上述已列明部分外，这些目的还包括：向用户提供项目或服务，包括列明定制的内容和广告；审计、调研和分析，以维持、保护和改善18ico的服务；确保网站的技术运作；开发新服务；以及其他18ico运营所需要的目的。</p>
                                </div>
                            </div>
                            <div class="article bottom-line">
                                <div class="title"><strong><span class="wysiwyg-font-size-large">信息公开与共享</span></strong>
                                </div>
                                <div>
                                    <p class="paragraph-title mt20">
                                        <strong>18ico不会将您的个人信息故意透露、出租或出售给任何第三方。但以下情况除外：</strong></p>
                                    <ul>
                                        <li>
                                            <p class="li-paragraph">用户本人同意与第三方共享信息；</p>
                                        </li>
                                        <li>
                                            <p class="li-paragraph">只有透露用户的个人信息，才能提供用户所要求的某种项目和服务；</p>
                                        </li>
                                        <li>
                                            <p class="li-paragraph">
                                                应代表18ico提供项目或服务的主体的要求提供（除非我们另行通知，否则该等主体无权将相关用户个人信息用于提供项目和服务之外的其他用途）：根据法律法规或行政命令的要求提供；因外部审计需要而提供；用户违反了18ico服务条款或任何其他项目及服务的使用规定；经18ico评估，用户的帐户存在风险，需要加以保护。</p>
                                        </li>
                                    </ul>
                                </div>
                            </div>
                            <div class="article bottom-line">
                                <div class="title">
                                    <strong><span class="wysiwyg-font-size-large">编辑和删除个人帐户资料的权限</span></strong></div>
                                <div>
                                    <p class="paragraph">
                                        您有权在任何时候编辑您在18ico的帐户信息，您也可以填写相关申请表格，要求删除个人帐户，但是您无条件同意在您的帐户删除后，该帐户内及与该帐户相关的信息仍然保留在18ico档案记录中，除上述第三条规定的情况外，我们将为您保密。</p>
                                </div>
                            </div>
                            <div class="article bottom-line">
                                <div class="title"><strong><span class="wysiwyg-font-size-large">安全保障</span></strong>
                                </div>
                                <div>
                                    <p class="paragraph">您的18ico帐户具有密码保护功能，以确保您的隐私及信息安全。</p>
                                </div>
                            </div>
                            <div class="article bottom-line">
                                <div class="title">
                                    <strong><span class="wysiwyg-font-size-large">隐私权政策的修订</span></strong></div>
                                <div>
                                    <p class="paragraph">18ico会不时对隐私权政策进行修改。如有修改，我们会在修改后及时公告相关修改内容及新规定，以便您知悉和使用。</p>
                                </div>
                            </div>
