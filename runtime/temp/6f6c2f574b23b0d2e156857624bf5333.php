                                <div class="title">
                                    <span class="wysiwyg-font-size-large"><strong>协议生效和适用范围</strong></span></div>
                                <div>
                                    <ul>
                                        <li>
                                            <p class="paragraph"><span class="paragraph-con">本协议内容包括协议正文以及18ico已经发布的或将来可能发布的各类规则、操作流程。所有规则为本协议不可分割的一部分，与协议正文具有同等的法律效力。发起者通过网络页面点击确认或以其他方式选择接受本协议，即表示您同意接受本协议（包括修订后的各项协议、规则和流程）的全部内容。</span>
                                            </p>
                                        </li>
                                        <li>
                                            <p class="paragraph"><span class="paragraph-con">当发起者在18ico区块链平台发起项目时，即表示同意与18ico达成协议并接受本协议的全部约定内容。</span>
                                            </p>
                                        </li>
                                        <li>
                                            <p class="paragraph"><span class="paragraph-con">18ico有权根据需要不时修改本协议或各类规则、操作流程，如本协议有任何变更，18ico将在网站上以公示形式通知，且无需征得发起者的事先同意。修改后的协议及规则一经公示即生效，成为本协议的一部分。如发起者继续登录或使用18ico平台的，即视为已阅读并接受修改后的协议。</span>
                                            </p>
                                        </li>
                                        <li>
                                            <p class="paragraph"><span class="paragraph-con">发起者应该按照本协议约定行使权利并履行义务。如不能接受本协议的约定，包括但不限于不能接受修订后的协议及各类规则，则应立即停止使用18ico平台提供的服务。如发起者继续使用服务，则表示同意并接受本协议及各类规则的约束。</span>
                                            </p>
                                        </li>
                                    </ul>
                                </div>
